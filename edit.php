                        // can scroll again
                        canScroll();
                    },
                    onBeforeClose : function() {
                        // don't allow to scroll
                        noScroll();
                    },
                    onAfterClose : function() {
                        // can scroll again
                        canScroll();
                    }
                }, false );
                
                var el = document.querySelector( '.morph-sidebar' );
                var sidebar = new UIMorphingButton( el, {
                    closeEl : '.icon-close',
                    onBeforeOpen : function() {
                        // don't allow to scroll
                        noScroll();
                        // push main container
                        classie.addClass( container, 'pushed' );
                    },
                    onAfterOpen : function() {
                        // can scroll again
